import math
#Задание №2

a = 4

b = 12

c = 3

D = b ** 2 - 4 * a * c  #считаем дискриминант

print(D) #дискриминант >0 считаем корни

x1 = (-b + math.sqrt(D)) / (2 * a)

x2 = (-b - math.sqrt(D)) / (2 * a)

print((round(x1, 2)), (round(x2, 2)))  #выводим корни с округлением до 2-ух знаков после запятой
